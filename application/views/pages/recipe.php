                <div class="tab-pane fade" id="step-by-step">
                    <h2 tabindex="8">Instructions:</h2>
                    <ol tabindex="9">
                        <li>Get one 3-quart saucepan</li>
                        <li>Mince 1 small onion finely</li>
                        <li>Heat saucepan on low heat</li>
                        <li>Add 1 ½ pounds of ground lean pork to saucepan</li>
                        <li>Mix in 1 minced onion to <span class="the" data-toggle="tooltip" data-placement="top" title="" data-original-title="loading synonyms...">meat</span></li>
                        <li>Mix in 1 ½ teaspoons of salt.</li>
                        <li>Mix in ¼ teaspoon of celery salt</li>
                        <li>Mix in ¼ teaspoon of back pepper</li>
                        <li>Mix in ¼ teaspoon of sage</li>
                        <li>Mix in 1 knife-tip of ground cloves (1/16 teaspoon)</li>
                        <li>Chop 1 garlic clove</li>
                        <li>Mix in 1 garlic clove</li>
                        <li>Cook while stiring slowly on low heat. Stop when <span class="the" data-toggle="tooltip" data-placement="top" title="" data-original-title="loading synonyms...">meat</span> has turned brown and half of the liquid has evaporated.</li>
                        <li>Cover pan and cook on low heat for 45 minutes.</li>
                        <li>While <span class="the" data-toggle="tooltip" data-placement="top" title="" data-original-title="loading synonyms...">meat</span> is cooking boil a large pot of water.</li>
                        <li>Place 3 potatoes in pot and boil until soft.</li>
                        <li>Mash potatoes.</li>
                        <li>When <span class="the" data-toggle="tooltip" data-placement="top" title="" data-original-title="loading synonyms...">meat</span> is done cooking, mix in potatoes and allow to cool.</li>
                        <li>While cooling, make pie crust. Begin by filling mixing <span class="the" data-toggle="tooltip" data-placement="top" title="" data-original-title="loading synonyms...">bowl</span> with 2 ½ cups of flour and ¾ teaspoon of salt.</li>
                        <li>Cube ⅔ cup of butter</li>
                        <li>Cube ⅓ cup of lard</li>
                        <li>Blend in butter and lard to flour mixture</li>
                        <li>Drizzle in small amounts of water while mixing until you reach ⅓ cup. You may need to mix in an additional 1 teaspoon of water. Mix until the texture is coarse.</li>
                        <li>Divide dough into 2.</li>
                        <li>Roll dough into disks.</li>
                        <li>Cover dough with cling film and allow to cool in the fridge for 30 min.</li>
                        <li>Roll one dough out to cover 9-inch pie plate.</li>
                        <li>Cover 9-inch pie plate with pie crust doug.</li>
                        <li>Add the cooled <span class="the" data-toggle="tooltip" data-placement="top" title="" data-original-title="loading synonyms...">meat</span> and potatoe mixture.</li>
                        <li>Roll one dough out to form the top of the pie.</li>
                        <li>Cover pie top.</li>
                        <li>Flute and seal the pie.</li>
                        <li>Preheat oven to 450 degrees F.</li>
                        <li>Cook pie for 10 minutes at 450 degrees F.</li>
                        <li>Reduce heat to 350 degrees F.</li>
                        <li>Cook for 30-40 min at 350 degrees F.</li>
                    </ol>
